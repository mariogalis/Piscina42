int	ft_str_is_alpha(char *a)
{
	int	i;

	i = 0;
	while (a[i] != '\0')
	{
		if ((a[i] >= 'A' && a[i] <= 'Z') || (a[i] >= 'a' && a[i] <= 'z'))
			i++;
		else
			return (0);
	}
	return (1);
}
